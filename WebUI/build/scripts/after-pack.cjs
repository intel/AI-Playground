/**
 * electron-builder afterPack hook (Linux only).
 *
 * Bakes `--no-sandbox` into the launcher so the AppImage runs when
 * double-clicked, not just when started from a terminal with the flag.
 *
 * Chromium initialises its Linux sandbox during early ContentMain startup —
 * before the Electron main-process JS runs — so `app.commandLine.appendSwitch
 * ('no-sandbox')` in electron/main.ts is too late to prevent the SUID/namespace
 * sandbox crash. On Linux `--no-sandbox` must be a real argv entry. The
 * `executableArgs` config option only reaches the generated .desktop `Exec=`
 * line, not the binary the AppImage's AppRun execs on a plain double-click.
 *
 * Fix: rename the real Electron binary to `<name>.bin` and replace it with a
 * shell wrapper of the same name that execs the real binary with `--no-sandbox`
 * always prepended. The AppRun (and the .desktop Exec) invoke `<name>`, so the
 * flag is injected regardless of how the app is launched.
 */
exports.default = async function afterPack(context) {
  if (context.electronPlatformName !== 'linux') return

  const fs = require('fs')
  const path = require('path')

  const exe = context.packager.executableName
  const launcher = path.join(context.appOutDir, exe)
  const realBinary = path.join(context.appOutDir, `${exe}.bin`)

  if (!fs.existsSync(launcher)) {
    throw new Error(`afterPack: expected Electron binary at "${launcher}" but it was not found`)
  }

  fs.renameSync(launcher, realBinary)

  const wrapper = `#!/bin/bash
# Auto-generated by build/scripts/after-pack.cjs — do not edit.
# Always pass --no-sandbox: Chromium's Linux sandbox is set up before the app's
# JS runs, so the flag must be on the real command line (works on double-click).
HERE="$(dirname "$(readlink -f "$0")")"
exec "$HERE/${exe}.bin" --no-sandbox "$@"
`

  fs.writeFileSync(launcher, wrapper)
  fs.chmodSync(launcher, 0o755)

  console.log(`  • baked --no-sandbox into Linux launcher "${exe}"`)
}
